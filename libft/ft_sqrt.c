#include "libft.h"

double	ft_sqrt(double x)
{
	double	xn;
	double	y;

	xn = x;
	y = x;
	while (y * y - x > 0.0000001)
	{
		y = 0.5 * (xn + x / xn);
		xn = y;
	}
	return (y);
}
